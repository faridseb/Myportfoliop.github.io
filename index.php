        const contactForm = document.getElementById('contactForm');

        // Gérer la soumission du formulaire
        contactForm.addEventListener('submit', function(event) {
            event.preventDefault(); // Empêche l'envoi traditionnel du formulaire (qui rechargerait la page)

            const formData = new FormData(this); // Récupère toutes les données du formulaire

            fetch(this.action, {
                method: this.method,
                body: formData
            })
            .then(response => {
                // Vérifie si la réponse est OK (statut HTTP 200)
                if (response.ok) {
                    return response.text(); // Lit la réponse du serveur comme texte
                }
                throw new Error('Erreur réseau ou réponse serveur incorrecte.');
            })
            .then(data => {
                // Ici, 'data' contient la réponse de votre script PHP
                // Si PHP renvoie "succès", on affiche l'alerte
                if (data.includes("succès")) { // Assurez-vous que votre PHP renvoie "succès" en cas de réussite
                    alert("Votre message a été envoyé avec succès !"); // Affiche l'alerte
                    contactForm.reset(); // Réinitialise le formulaire après succès
                } else {
                    alert("Une erreur est survenue lors de l'envoi : " + data);
                }
            })
            .catch(error => {
                console.error('Erreur lors de la soumission du formulaire:', error);
